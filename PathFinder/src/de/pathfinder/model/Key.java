package de.pathfinder.model;

public class Key {

	long k1, k2;

	public Key() {

	}

	public Key(long k1, long k2) {
		this.k1 = k1;
		this.k2 = k2;
	}

	public long getK1() {
		return k1;
	}

	public void setK1(long k1) {
		this.k1 = k1;
	}

	public long getK2() {
		return k2;
	}

	public void setK2(long k2) {
		this.k2 = k2;
	}

}
